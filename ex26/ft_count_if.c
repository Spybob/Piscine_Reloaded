int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int t;

	i = 0;
	t = 0;
	while (tab[i])
	{
		if ((*f)(tab[i]) == 1)
			t++;
		i++;
	}
	return (t);
}
