int	ft_sqrt(int nb)
{
	int i;

	i = 2;
	if (nb < 0 || nb == 2 || nb == 3)
		return (0);
	if (nb == 0 || nb == 1)
		return (nb);
	while (i * i <= nb && i < 46340)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
